// Variables con let
let mensaje = "Hola, mundo";
console.log(mensaje); // Hola, mundo

// Reasignación de valor
mensaje = "Aprendiendo JavaScript";
console.log(mensaje); // Aprendiendo JavaScript

// Variable con const
